const GRIDS = {
  human_male: parseGrid(humanMaleGridStr),
  human_female: parseGrid(humanFemaleGridStr),
  android: parseGrid(androidGridStr),
  ethereal: parseGrid(etherealGridStr),
};

export const PixelAvatar: React.FC<PixelAvatarProps> = React.memo(({ config, size = 64, className = '' }) => {
  if (config.customAvatar) {
    return (
      <img
        src={config.customAvatar}
        width={size}
        height={size}
        alt="Custom Avatar"
        className={`rounded-md object-cover ${className}`}
        style={{ imageRendering: 'pixelated' }}
      />
    );
  }

  // Dual support for config.archetype or config.type
  const archetypeKey = config.archetype || (config as any).type || 'human_male';
  const grid = GRIDS[archetypeKey as keyof typeof GRIDS] || GRIDS.human_male;
  const gridSize = 32;

  // Compute color palette based on config.color
  const palette = useMemo(() => {
    const primary = config.color || '#3b82f6';
    
    const isEthereal = archetypeKey === 'ethereal';
    const isAndroid = archetypeKey === 'android';
    const isFemale = archetypeKey === 'human_female';
    
    let skinTone = '#FFCD94'; // default warm human skin tone
    let shadowTone = '#E0A060';
    let shirt = hexToRgba(primary, 0.8);
    
    if (isAndroid) {
      skinTone = '#94a3b8'; // slate 400
      shadowTone = '#64748b'; // slate 500
      shirt = '#334155'; // slate 700
    } else if (isEthereal) {
      skinTone = hexToRgba(primary, 0.6);
      shadowTone = hexToRgba(primary, 0.9);
      shirt = primary;
    } else if (isFemale) {
      skinTone = '#FFE0BD'; // soft warm skin
      shadowTone = '#F5C07A';
    }

    return {
      0: 'transparent',
      1: primary, // Hair / main theme
      2: skinTone,
      3: isFemale ? '#F59E0B' : '#ffffff', // Gold earrings for female, white for eyes
      4: shadowTone,
      5: isAndroid ? '#0f172a' : '#262626', // dark outline / pupils
      6: shirt,
    };
  }, [archetypeKey, config.color]);

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${gridSize} ${gridSize}`}
      className={`rounded-md ${className}`}
      style={{ imageRendering: 'pixelated' }}
      xmlns="http://www.w3.org/2000/svg"
    >
      {grid.map((row, y) =>
        row.map((cell, x) => {
          if (cell === 0) return null;
          return (
            <rect
              key={`${x}-${y}`}
              x={x}
              y={y}
              width="1"
              height="1"
              fill={palette[cell as keyof typeof palette]}
            />
          );
        })
      )}
    </svg>
  );
});

PixelAvatar.displayName = 'PixelAvatar';
